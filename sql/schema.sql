DROP TABLE IF EXISTS group_mappings; 
DROP TABLE IF EXISTS users;	
DROP TABLE IF EXISTS Member;
CREATE TABLE Member (
    MemberID INT PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    Image VARCHAR(255),
    Age INT NOT NULL,
    Gender VARCHAR(10),
    Email VARCHAR(100) NOT NULL UNIQUE,
    ContactNumber VARCHAR(15) NOT NULL,
    MemberType VARCHAR(20) NOT NULL CHECK (MemberType IN ('Passenger', 'Driver')),
    RegistrationDate DATE NOT NULL
);
CREATE TABLE Users (
    UserID    INTEGER PRIMARY KEY,
    username  TEXT NOT NULL UNIQUE,
    password_hash TEXT NOT NULL,
    role      TEXT NOT NULL CHECK(role IN ('admin', 'user')),
    MemberID  INTEGER UNIQUE,
    FOREIGN KEY (MemberID) REFERENCES Member(MemberID) ON DELETE CASCADE
);
CREATE TABLE group_mappings (
    MappingID  INTEGER PRIMARY KEY,
    UserID     INTEGER NOT NULL,
    group_name TEXT NOT NULL,
    FOREIGN KEY (UserID) REFERENCES users(UserID) ON DELETE CASCADE
);
CREATE TABLE Passenger (
    PassengerID INT PRIMARY KEY,
    MemberID INT NOT NULL UNIQUE,
    EmergencyContact VARCHAR(15),
    PreferredPaymentMethod VARCHAR(20) NOT NULL,
    SpecialAssistance VARCHAR(100),
    NotificationPreference VARCHAR(20) NOT NULL CHECK (NotificationPreference IN ('SMS', 'Email', 'App', 'All')),
    Status_ VARCHAR(20) NOT NULL CHECK (Status_ IN ('Active', 'Inactive', 'Suspended')),
    FOREIGN KEY (MemberID) REFERENCES Member(MemberID) ON DELETE CASCADE
);
CREATE TABLE Driver (
    DriverID INT PRIMARY KEY,
    MemberID INT NOT NULL UNIQUE,
    LicenseNumber VARCHAR(30) NOT NULL UNIQUE,
    LicenseExpiryDate DATE NOT NULL,
    ExperienceYears INT NOT NULL,
    Rating DECIMAL(3,2) DEFAULT 0.00,
    Status_ VARCHAR(20) NOT NULL CHECK (Status_ IN ('Off-Duty', 'Available', 'On-Trip')),
    FOREIGN KEY (MemberID) REFERENCES Member(MemberID) ON DELETE CASCADE,
    CHECK (ExperienceYears >= 0),
    CHECK (Rating >= 0.00 AND Rating <= 5.00)
);
CREATE TABLE Vehicle (
    VehicleID INT PRIMARY KEY,
    VehicleNumber VARCHAR(20) NOT NULL UNIQUE,
    Model VARCHAR(50) NOT NULL,
    Capacity INT NOT NULL,
    CurrentStatus VARCHAR(20) NOT NULL CHECK (CurrentStatus IN ('Active', 'Maintenance', 'OutOfService')),
    GPSDeviceID VARCHAR(50),
    RegistrationDate DATE NOT NULL,
    CHECK (Capacity > 0)
);
CREATE TABLE VehicleLiveLocation (
    LocationID INT PRIMARY KEY,
    VehicleID INT NOT NULL,
    Latitude DECIMAL(9,6) NOT NULL,
    Longitude DECIMAL(9,6) NOT NULL,
    Timestamp TIMESTAMP NOT NULL,
    Speed DECIMAL(5,2),
    FOREIGN KEY (VehicleID) REFERENCES Vehicle(VehicleID) ON DELETE CASCADE,
    CHECK (Latitude >= -90 AND Latitude <= 90),
    CHECK (Longitude >= -180 AND Longitude <= 180),
    CHECK (Speed >= 0)
);
CREATE TABLE VehicleMaintenance (
    MaintenanceID INT PRIMARY KEY,
    VehicleID INT NOT NULL,
    ServiceDate DATE NOT NULL,
    ServiceType VARCHAR(50) NOT NULL,
    Cost NUMERIC(8,2) NOT NULL,
    NextServiceDue DATE NOT NULL,
    Status_ VARCHAR(20) NOT NULL CHECK (Status_ IN ('Scheduled', 'Completed', 'Pending')),
    FOREIGN KEY (VehicleID) REFERENCES Vehicle(VehicleID) ON DELETE RESTRICT,
    CHECK (Cost >= 0),
    CHECK (NextServiceDue > ServiceDate)
);
CREATE TABLE Route (
    RouteID INT PRIMARY KEY,
    RouteName VARCHAR(100) NOT NULL,
    Source VARCHAR(50) NOT NULL,
    Destination VARCHAR(50) NOT NULL,
    IntermediateStops TEXT,
    EstimatedDuration INT NOT NULL,
    DistanceKM DECIMAL(6,2) NOT NULL,
    BaseFare NUMERIC(8,2) NOT NULL,
    Status_ VARCHAR(20) NOT NULL CHECK (Status_ IN ('Active', 'Inactive')),
    CHECK (EstimatedDuration > 0),
    CHECK (DistanceKM > 0),
    CHECK (BaseFare > 0)
);
CREATE TABLE Trip (
    TripID INT PRIMARY KEY,
    RouteID INT NOT NULL,
    VehicleID INT NOT NULL,
    DriverID INT NOT NULL,
    TripDate DATE NOT NULL,
    ScheduledDepartureTime TIME NOT NULL,
    ScheduledArrivalTime TIME NOT NULL,
    ActualDepartureTime TIME,
    ActualArrivalTime TIME,
    Status_ VARCHAR(20) NOT NULL CHECK (Status_ IN ('Scheduled', 'InProgress', 'Completed', 'Cancelled')),
    TotalSeats INT NOT NULL,
    AvailableSeats INT NOT NULL,
    FOREIGN KEY (RouteID) REFERENCES Route(RouteID) ON DELETE RESTRICT,
    FOREIGN KEY (VehicleID) REFERENCES Vehicle(VehicleID) ON DELETE RESTRICT,
    FOREIGN KEY (DriverID) REFERENCES Driver(DriverID) ON DELETE RESTRICT,
    CHECK (ScheduledArrivalTime > ScheduledDepartureTime),
    CHECK (AvailableSeats >= 0),
    CHECK (AvailableSeats <= TotalSeats),
    CHECK (TotalSeats > 0),
    CHECK (ActualArrivalTime is NULL OR ActualArrivalTime > ActualDepartureTime)
);
CREATE TABLE TripOccupancyLog (
    LogID INT PRIMARY KEY,
    TripID INT NOT NULL,
    LogTimestamp TIMESTAMP NOT NULL,
    OccupiedSeats INT NOT NULL,
    AvailableSeats INT NOT NULL,
    FOREIGN KEY (TripID) REFERENCES Trip(TripID) ON DELETE CASCADE,
    CHECK (OccupiedSeats >= 0),
    CHECK (AvailableSeats >= 0)
);
CREATE TABLE DriverAssignment (
    AssignmentID INT PRIMARY KEY,
    DriverID INT NOT NULL,
    VehicleID INT NOT NULL,
    TripID INT NOT NULL,
    AssignedDate DATE NOT NULL,
    ShiftStart TIME NOT NULL,
    ShiftEnd TIME NOT NULL,
    Status_ VARCHAR(20) NOT NULL CHECK (Status_ IN ('Assigned', 'Completed', 'Cancelled')),
    FOREIGN KEY (DriverID) REFERENCES Driver(DriverID) ON DELETE RESTRICT,
    FOREIGN KEY (VehicleID) REFERENCES Vehicle(VehicleID) ON DELETE RESTRICT,
    FOREIGN KEY (TripID) REFERENCES Trip(TripID) ON DELETE CASCADE,
    CHECK (ShiftEnd > ShiftStart)
);
CREATE TABLE Booking (
    BookingID INT PRIMARY KEY,
    PassengerID INT NOT NULL,
    TripID INT NOT NULL,
    SeatNumber INT NOT NULL,
    BookingTime TIMESTAMP NOT NULL,
    BookingStatus VARCHAR(20) NOT NULL CHECK (BookingStatus IN ('Confirmed', 'Cancelled', 'NoShow', 'Completed')),
    FareAmount NUMERIC(8,2) NOT NULL,
    QRCode TEXT NOT NULL UNIQUE,
    QRCodeURL VARCHAR(255),
    VerificationStatus VARCHAR(20) NOT NULL CHECK (VerificationStatus IN ('Pending', 'Verified', 'Invalid')),
    VerifiedAt TIMESTAMP,
    VerifiedBy INT,
    FOREIGN KEY (PassengerID) REFERENCES Passenger(PassengerID) ON DELETE RESTRICT,
    FOREIGN KEY (TripID) REFERENCES Trip(TripID) ON DELETE RESTRICT,
    FOREIGN KEY (VerifiedBy) REFERENCES Driver(DriverID) ON DELETE SET NULL,
    UNIQUE (TripID, SeatNumber),
    CHECK (FareAmount >= 0),
    CHECK (SeatNumber > 0)
);
CREATE TABLE `Transaction` (
    TransactionID INT PRIMARY KEY,
    BookingID INT NOT NULL,
    TransactionType VARCHAR(20) NOT NULL CHECK (TransactionType IN ('Payment', 'Refund', 'Penalty')),
    Amount NUMERIC(8,2) NOT NULL,
    TransactionDate TIMESTAMP NOT NULL,
    PaymentMethod VARCHAR(20) NOT NULL CHECK (PaymentMethod IN ('Card', 'UPI', 'Wallet', 'Cash')),
    PaymentStatus VARCHAR(20) NOT NULL CHECK (PaymentStatus IN ('Pending', 'Success', 'Failed')),
    GatewayReference VARCHAR(100),
    FOREIGN KEY (BookingID) REFERENCES Booking(BookingID) ON DELETE RESTRICT,
    CHECK (Amount >= 0)
);
CREATE TABLE BookingCancellation (
    CancellationID INT PRIMARY KEY,
    BookingID INT NOT NULL,
    CancellationTime TIMESTAMP NOT NULL,
    CancellationReason VARCHAR(200) NOT NULL,
    RefundAmount NUMERIC(8,2) NOT NULL,
    PenaltyAmount NUMERIC(8,2) NOT NULL,
    ProcessedAt TIMESTAMP,
    Status_ VARCHAR(20) NOT NULL CHECK (Status_ IN ('Pending', 'Processed', 'Rejected')),
    FOREIGN KEY (BookingID) REFERENCES Booking(BookingID) ON DELETE RESTRICT,
    CHECK (RefundAmount >= 0),
    CHECK (PenaltyAmount >= 0)
);
CREATE TABLE NoShowPenalty (
    PenaltyID INT PRIMARY KEY,
    BookingID INT NOT NULL,
    DetectionTime TIMESTAMP NOT NULL,
    PenaltyAmount NUMERIC(8,2) NOT NULL,
    Reason VARCHAR(200) NOT NULL,
    AutoGenerated BOOLEAN NOT NULL,
    PaymentStatus VARCHAR(20) NOT NULL CHECK (PaymentStatus IN ('Pending', 'Paid', 'Waived')),
    WaivedBy INT,
    FOREIGN KEY (BookingID) REFERENCES Booking(BookingID) ON DELETE RESTRICT,
    CHECK (PenaltyAmount >= 0)
);
